package BasicMaths;

public class BitwiseAnd {
	
	public static void main(String args[ ]) {
		
		int a = 3;
		 int b = 6;
		 int c = a & b;
		 System.out.println(c);
		 System.out.println(a | b);
		 System.out.println(a^b);
		// a= a<<2;
		 System.out.println(a<<2);
		 System.out.println(b>>68);
		 
	}

}
